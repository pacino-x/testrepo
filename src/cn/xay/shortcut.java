/**
 * public static void main(String[] args) {
 * 这个常用的语句，快捷输入方法为，psvm+enter
 * System.out.println();
 * 这个语句，快捷输入方法为，sout+enter
 * 这样很快
 */

public class shortcut {
    public static void main(String[] args) {
        System.out.println("line1");
        System.out.println("line2");
        System.out.println("line3");

    }
}
